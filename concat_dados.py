# receber dois dados do usuario e concatenar em uma unica String

# Recebe dois dados do usuário
primeira_entrada = input("Digite a primeira informação: ")
segunda_entrada = input("Digite a segunda informação: ")

# Concatena as duas entradas em uma única string
resultado = primeira_entrada + " " + segunda_entrada

# Exibe o resultado
print("Resultado da concatenação: ", resultado)

